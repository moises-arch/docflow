// Render a Walmart order to PDF. Builds a clean HTML template and renders it
// with Playwright (no login, no remote navigation — just setContent + page.pdf).
//
// Failure to render is non-fatal: returns null and the caller continues
// without a PDF. The order_draft is the source of truth, the PDF is just an
// archive copy.

import chromium from "@sparticuz/chromium";
import { chromium as playwrightChromium } from "playwright-core";
import type { WalmartParsed } from "@/lib/walmart/parse-order";

function formatCurrency(amount: number, currency = "USD"): string {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency,
    minimumFractionDigits: 2,
  }).format(amount);
}

function formatDate(iso: string | null): string {
  if (!iso) return "—";
  return new Date(iso).toLocaleString("en-US", {
    dateStyle: "short",
    timeStyle: "short",
  });
}

function escape(s: string | null | undefined): string {
  if (!s) return "";
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

