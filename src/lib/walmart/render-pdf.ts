async function launchBrowser() {
  const executablePath = process.env.PLAYWRIGHT_CHROMIUM_EXECUTABLE_PATH;
  if (executablePath) {
    return playwrightChromium.launch({ executablePath, headless: true });
  }
  if (!process.env.VERCEL) {
    try {
      return await playwrightChromium.launch({ headless: true });
    } catch {
      // fall through to packaged
    }
  }
  return playwrightChromium.launch({
    args: chromium.args,
    executablePath: await chromium.executablePath(),
    headless: true,
  });
}

/**
 * Render a Walmart parsed order to PDF.
 * Returns null on failure (non-fatal — caller continues without PDF).
 */
export async function renderWalmartOrderToPdf(
  parsed: WalmartParsed,
): Promise<Buffer | null> {
  let browser: Awaited<ReturnType<typeof launchBrowser>> | null = null;
  try {
    browser = await launchBrowser();
    const context = await browser.newContext({ viewport: { width: 1056, height: 1400 } });
    const page = await context.newPage();
    const html = renderHtml(parsed);

    await page.setContent(html, { waitUntil: "networkidle", timeout: 15_000 });
    const pdf = await page.pdf({
      format: "Letter",
      printBackground: true,
      margin: { top: "0", bottom: "0", left: "0", right: "0" },
    });
    if (!pdf || pdf.length < 4 || pdf.subarray(0, 4).toString() !== "%PDF") {
      return null;
    }
    return Buffer.from(pdf);
  } catch (err) {
    console.warn("walmart_pdf_render_warning:", err);
    return null;
  } finally {
    if (browser) await browser.close().catch(() => {});
  }
}
